    public void loadSalesReport(Date from, Date to, String search) {
        try (Connection con = DriverManager.getConnection(
                "jdbc:mysql://localhost:3306/StudEssentials_POS", "root", "admin123")) {

            String sql = "SELECT cashier_username, product_name, quantity, price, total, timestamp "
                    + "FROM sales WHERE timestamp BETWEEN ? AND ? AND product_name LIKE ?";

            PreparedStatement pst = con.prepareStatement(sql);
            // From date: start of the day
            Calendar calFrom = Calendar.getInstance();
            calFrom.setTime(from);
            calFrom.set(Calendar.HOUR_OF_DAY, 0);
            calFrom.set(Calendar.MINUTE, 0);
            calFrom.set(Calendar.SECOND, 0);
            calFrom.set(Calendar.MILLISECOND, 0);

            // To date: end of the day
            Calendar calTo = Calendar.getInstance();
            calTo.setTime(to);
            calTo.set(Calendar.HOUR_OF_DAY, 23);
            calTo.set(Calendar.MINUTE, 59);
            calTo.set(Calendar.SECOND, 59);
            calTo.set(Calendar.MILLISECOND, 999);

            pst.setTimestamp(1, new java.sql.Timestamp(calFrom.getTimeInMillis()));
            pst.setTimestamp(2, new java.sql.Timestamp(calTo.getTimeInMillis()));
            
            pst.setString(3, "%" + search + "%");
            
            ResultSet rs = pst.executeQuery();

            DefaultTableModel model = (DefaultTableModel) tblSales.getModel();
            model.setRowCount(0); // clear table

            int count = 0;
            while (rs.next()) {
                count++;
                model.addRow(new Object[]{
                    rs.getString("cashier_username"),
                    rs.getString("product_name"),
                    rs.getInt("quantity"),
                    rs.getDouble("price"),
                    rs.getDouble("total"),
                    rs.getTimestamp("timestamp")
                });
            }
            System.out.println("Loaded rows: " + count);  // <-- check console output

            rs.close();
            pst.close();

        } catch (SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error loading sales report: " + e.getMessage());
        }
    }

    private void calculateSalesSummary() {
        double totalSales = 0.0;
        int totalItems = 0;

        for (int i = 0; i < tblSales.getRowCount(); i++) {
            Object qtyObj = tblSales.getValueAt(i, 2);    // Qty column
            Object totalObj = tblSales.getValueAt(i, 4);  // Total column

            if (qtyObj != null && totalObj != null) {
                try {
                    int qty = Integer.parseInt(qtyObj.toString());
                    double total = Double.parseDouble(totalObj.toString());

                    totalItems += qty;
                    totalSales += total;
                } catch (NumberFormatException e) {
                    System.out.println("Error parsing row " + i);
                }
            }
        }

        lblTOTALITEMS.setText(String.valueOf(totalItems));
        lblTOTALSALES.setText("₱ " + String.format("%.2f", totalSales));
    }



    
