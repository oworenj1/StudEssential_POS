    private void btnEXPORTActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEXPORTActionPerformed
        // TODO add your handling code here:
        JFileChooser fileChooser = new JFileChooser();
        fileChooser.setDialogTitle("Save Sales Report");
        int userSelection = fileChooser.showSaveDialog(this);

        if (userSelection == JFileChooser.APPROVE_OPTION) {
            File fileToSave = fileChooser.getSelectedFile();

            try (PrintWriter pw = new PrintWriter(new FileWriter(fileToSave + ".csv"))) {
                // Header
                for (int i = 0; i < tblSales.getColumnCount(); i++) {
                    pw.print(tblSales.getColumnName(i));
                    if (i != tblSales.getColumnCount() - 1) {
                        pw.print(",");
                    }
                }
                pw.println();

                // Rows
                for (int i = 0; i < tblSales.getRowCount(); i++) {
                    for (int j = 0; j < tblSales.getColumnCount(); j++) {
                        pw.print(tblSales.getValueAt(i, j));
                        if (j != tblSales.getColumnCount() - 1) {
                            pw.print(",");
                        }
                    }
                    pw.println();
                }

                JOptionPane.showMessageDialog(this, "Sales report exported successfully!");

            } catch (IOException e) {
                JOptionPane.showMessageDialog(this, "Error exporting file: " + e.getMessage());
            }
        }
    }//GEN-LAST:event_btnEXPORTActionPerformed

