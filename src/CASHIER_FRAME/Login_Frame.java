    private void btnENTERActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnENTERActionPerformed
        // TODO add your handling code here:
        try {
            String username = txtUSERNAME.getText().trim();
            String password = new String(txtPASSWORD.getPassword()).trim();

            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/StudEssentials_POS", "root", "admin123");
            String sql = "SELECT * FROM users WHERE username = ? AND password = ?";
            PreparedStatement pst = con.prepareStatement(sql);
            pst.setString(1, username);
            pst.setString(2, password);

            ResultSet rs = pst.executeQuery();

            if (rs.next()) {
                String role = rs.getString("role");
                String firstName = rs.getString("firstname");  // adjust to your actual column name
                String lastName = rs.getString("surname");     // adjust to your actual column name
                String fullName = firstName + " " + lastName;

                // Show welcome message
                JOptionPane.showMessageDialog(null, "Login successful! Welcome " + fullName + "!");

                // Logging activity
                logActivity(username, "Logged in");

                if (role.equals("admin")) {
                    ADMIN admin = new ADMIN(username);
                    admin.setVisible(true);
                    this.dispose();

                } else if (role.equals("cashier")) {
                    POS ps = new POS();
                    ps.setCurrentUsername(username);
                    ps.setVisible(true);
                    this.dispose();
                }

            } else {
                JOptionPane.showMessageDialog(null, "Invalid username or password");
            }

            rs.close();
            pst.close();
            con.close();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error: " + e.getMessage());
            e.printStackTrace();
        }
        
    }//GEN-LAST:event_btnENTERActionPerformed

