    public void login(String username, String password) {
        try (Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/pos_system", "root", "admin123"); PreparedStatement pst = conn.prepareStatement("SELECT * FROM users WHERE username=? AND password=?")) {

            pst.setString(1, username);
            pst.setString(2, password);

            ResultSet rs = pst.executeQuery();

            if (rs.next()) {
                
                logActivity(username, "Logged in");
                
                String role = rs.getString("role");
                if ("admin".equals(role)) {
                    new ADMIN().setVisible(true);
                } else if ("cashier".equals(role)) {
                    new POS().setVisible(true);
                }
            } else {
                JOptionPane.showMessageDialog(null, "Invalid login.");
            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }
    
    
    
    private void logActivity(String username, String action) {
        try (Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/StudEssentials_POS", "root", "admin123")) {
            String sql = "INSERT INTO activity_logs (username, action) VALUES (?, ?)";
            PreparedStatement pst = con.prepareStatement(sql);
            pst.setString(1, username);
            pst.setString(2, action);
            pst.executeUpdate();
            pst.close();
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }
    
